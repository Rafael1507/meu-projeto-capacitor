<h1>Variáveis</h1>

<ul>
  <li><strong>let:</strong> variável que pode mudar</li>
  <li><strong>const:</strong> valor fixo, não muda</li>
  <li><strong>var:</strong> antigo, não usar mais (problemas com escopo)</li>
</ul>

<pre><code>
let idade = 16;
const nome = "Maria";
var cidade = "Campo Grande";
</code></pre>
